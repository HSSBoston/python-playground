


class Star:
    def __init__(shape, center, xLeft, xRight, yTop, yBottom):
        self.shape = shape # "big-circle" "small-circle" or "rectangle"
        self.center = center
        self.xLeft = xLeft
        self.xRight = xRight
        self.yTop = yTop
        self.yBottom = yBottom
    
    def shape():
        return self.shape
    
    def center():
        return self.center
    
    def xLeft():
        return self.xLeft
    
    def xRight():
        return self.xRight
    
    def yTop():
        return self.yTop
    
    def yBottom():
        return self.yBottom
    